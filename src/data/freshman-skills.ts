// 全スキルをフラットに取得
export const getAllSkills = (): FreshmanSkill[] => {
  return freshmanCategories.flatMap((cat) => cat.skills);
};

// IDからスキルを取得
export const getSkillById = (id: string): FreshmanSkill | undefined => {
  return getAllSkills().find((skill) => skill.id === id);
};

// IDからカテゴリを取得
export const getCategoryBySkillId = (
  skillId: string
): FreshmanCategory | undefined => {
  return freshmanCategories.find((cat) =>
    cat.skills.some((skill) => skill.id === skillId)
  );
};

